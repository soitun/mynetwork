package rpc

import (
	"net"

	"github.com/libp2p/go-libp2p/core/peer"
)

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type Args struct {
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type StatusReply struct {
	PeerID              string
	SwarmPeersCurrent   int
	NetPeersCurrent     int
	NetPeerAddrsCurrent []string
	NetPeersMax         int
	ListenAddrs         []string
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type PeerInfo struct {
	PeerID   string
	Name     string
	IPv4     string
	IPv6     string
}

type PeersReply struct {
	Peers     []PeerInfo
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type RouteAction string

// -----------------------------------------------------------------------------------------------------------------------------------------------------
const (
	Show RouteAction = "show"
	Add  RouteAction = "add"
	Del  RouteAction = "del"
)

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type RouteInfo struct {
	Network     net.IPNet
	TargetName  string
	TargetAddr  peer.ID
	RelayAddr   peer.ID
	IsRelay     bool
	IsConnected bool
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type RouteArgs struct {
	Action RouteAction
	Args   []string
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type RouteReply struct {
	Out    string
	Routes []RouteInfo
	Err    error
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type AddPeerArgs struct {
	Name string
	ID   string
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type AddPeerReply struct {
	Success bool
	Message string
	Err     error
}

// -----------------------------------------------------------------------------------------------------------------------------------------------------
type NodeIpReply struct {
	IPv4 string
	IPv6 string
	Err  error
}
